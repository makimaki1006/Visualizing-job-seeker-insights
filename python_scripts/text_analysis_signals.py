def count_signals_in_text(text: str, signal_list: list[tuple]) -> dict:
    """テキスト内のシグナル出現を検出し、スコアを返す

    Args:
        text: 検索対象テキスト
        signal_list: [(語彙, weight, 説明), ...] のリスト

    Returns:
        {
            "total_score": weighted合計スコア,
            "signal_count": 検出されたシグナル数,
            "signals_found": [(語彙, weight, 出現回数), ...],
        }
    """
    if not text:
        return {"total_score": 0, "signal_count": 0, "signals_found": []}

    total_score = 0
    found = []
    for term, weight, _desc in signal_list:
        if weight == 0:
            continue  # プレースホルダ（正規表現で別途処理）
        count = 0
        start = 0
        while True:
            idx = text.find(term, start)
            if idx == -1:
                break
            # 否定プレフィックスチェック（「不」「非」「未」「無」）
            if idx > 0 and text[idx - 1] in "不非未無":
                start = idx + len(term)
                continue
            count += 1
            start = idx + len(term)
        if count > 0:
            total_score += weight * count
            found.append((term, weight, count))

    return {
        "total_score": total_score,
        "signal_count": len(found),
        "signals_found": found,
    }


def flatten_signals(category_dict: dict) -> list[tuple]:
    """カテゴリ辞書からシグナルリストをフラット化

    subcategories構造またはsignals直下構造の両方に対応
    """
    signals = []
    if "subcategories" in category_dict:
        for _sub_name, sub_data in category_dict["subcategories"].items():
            signals.extend(sub_data["signals"])
    elif "signals" in category_dict:
        signals.extend(category_dict["signals"])
    elif "categories" in category_dict:
        for _cat_name, cat_data in category_dict["categories"].items():
            if isinstance(cat_data, dict) and "signals" in cat_data:
                signals.extend(cat_data["signals"])
            elif isinstance(cat_data, list):
                # TEMPLATE_PATTERNS形式（文字列リスト）
                signals.extend([(s, 1, "") for s in cat_data])
    return signals
